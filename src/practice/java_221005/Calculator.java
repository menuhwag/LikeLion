package practice.java_221005;

public class Calculator {
    public int plus(int a, int b) {
        return a + b;
    }
    public int minus(int a, int b) {
        return a - b;
    }
    public int multiple(int a, int b) {
        return a * b;
    }
    public double divide(int a, int b) {
        return a / (double)b;
    }
}
